const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const vacationSchema = new Schema(
  {
    destination: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    startDate: {
      type: Date,
      required: true,
    },
    endDate: {
      type: Date,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Vacation = mongoose.model('Vacation', vacationSchema);
module.exports = Vacation;
